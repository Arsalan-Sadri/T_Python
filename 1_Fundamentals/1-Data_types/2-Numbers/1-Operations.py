# type: class int
num = 3
print(type(num))

# type: class float
num = 3.4
print(type(num))

# Shorthand addition
num = 5
num += 3
print(num)

# Shorthand subtraction
num = 8
num -= 4
print(num)

# Shorthand multiplication
num = 3
num *= 10
print(num)

# shorthand division
num = 20
num /= 4
print(num)

# shorthand floor division
num = 20
num //= 4
print(num)

# Exponentiation 
num = 4
num **= 2
print(num)